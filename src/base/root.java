package base;

public class root {
    public long solution(long n) {
        double a = 0;
        long anwser = -1;
        if((Math.sqrt(n))%1==0){
            a = Math.sqrt(n);
            anwser = (long) ((a+1)*(a+1));
        } else {
            return anwser;
        }
        return anwser;
    }
}
